#------------------------------------------------------------------------------------------
# MAIN
#------------------------------------------------------------------------------------------
main()
{
	echo "Staring Nginx Server...."
	startNginxServer
}

#------------------------------------------------------------------------------------------
# INTERNAL FUNCTIONS
#------------------------------------------------------------------------------------------
function startNginxServer()
{
	workingDirectory=$(pwd)
	nginxConfigurationFile="${workingDirectory}/nginx_configuration/nginx_mac.conf"
	sudo nginx -c "$nginxConfigurationFile" 
}

#------------------------------------------------------------------------------------------
# SCRIPT
#------------------------------------------------------------------------------------------
    main
